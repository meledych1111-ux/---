/**
 * –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏ –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å —Ç–æ–≤–∞—Ä–∞–º–∏
 */
export const ProductUtils = {
    // –ü–æ–ª—É—á–∏—Ç—å —Ç–æ–≤–∞—Ä—ã –ø–æ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
    getByCategory: (category) => {
        if (category === 'all') return products;
        return products.filter(product => product.category === category);
    },

    // –ü–æ–∏—Å–∫ —Ç–æ–≤–∞—Ä–æ–≤
    search: (query) => {
        const searchTerm = query.toLowerCase();
        return products.filter(product =>
            product.name.toLowerCase().includes(searchTerm) ||
            product.description.toLowerCase().includes(searchTerm) ||
            product.features.some(feature => feature.toLowerCase().includes(searchTerm))
        );
    },

    // –§–∏–ª—å—Ç—Ä –ø–æ —Ü–µ–Ω–µ
    filterByPrice: (productsArray, priceRange) => {
        switch (priceRange) {
            case '0-5000':
                return productsArray.filter(product => product.price <= 5000);
            case '5000-10000':
                return productsArray.filter(product => product.price > 5000 && product.price <= 10000);
            case '10000+':
                return productsArray.filter(product => product.price > 10000);
            default:
                return productsArray;
        }
    },

    // –ü–æ–ª—É—á–∏—Ç—å —É–Ω–∏–∫–∞–ª—å–Ω—ã–µ –∫–∞—Ç–µ–≥–æ—Ä–∏–∏
    getCategories: () => {
        const categories = [...new Set(products.map(product => product.category))];
        return ['all', ...categories];
    },

    // –ü–æ–ª—É—á–∏—Ç—å —Ç–æ–≤–∞—Ä –ø–æ ID
    getById: (id) => {
        return products.find(product => product.id === id);
    },

    // –ü–æ–ª—É—á–∏—Ç—å –¥–æ—Å—Ç—É–ø–Ω—ã–µ —Ä–∞–∑–º–µ—Ä—ã –¥–ª—è —Ç–æ–≤–∞—Ä–∞
    getSizes: (productId) => {
        const product = products.find(p => p.id === productId);
        return product ? product.sizes : [];
    },

    // –ü–æ–ª—É—á–∏—Ç—å —Ç–æ–≤–∞—Ä—ã –ø–æ –º–∞—Å—Å–∏–≤—É ID
    getByIds: (ids) => {
        return products.filter(product => ids.includes(product.id));
    }
};

